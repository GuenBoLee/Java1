package week02.sec04;

public class Long01 {

	public static void main(String[] args) {
		long var1 = 40;
		long var2 = 50L;
		
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var1+var2);
		System.out.println(var1-var2);
		System.out.println(var1*var2);
		System.out.println(var1/var2);
		

	}

}
